#include <stdio.h>
#include "main.h"

/**
 * main - a program that prints its
 * name, followed by a new line.
 * argc: number of arguments supplied
 * argv: the array of arguments provided
 *
 * Return: Always 0 (Success)
 */
int main(int argc, char *argv[])
{
	argc = argc;
	printf("%s\n", *argv);
	return (0);
}
